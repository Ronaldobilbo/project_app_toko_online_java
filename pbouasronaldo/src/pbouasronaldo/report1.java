    private void cetak() {
    String html =
            "<table width='100%' height='100%' border='1'> "+
            "<tr>"
                +"<td>No Faktur</td>"
                +"<td>Tanggal transaksi</td>"
                +"<td>Jumlah transaksi</td>"
                +"<td>Kode pelanggan</td>"
                +"<td>Nama pelanggan</td>"
                +"<td>Kode barang</td>"
                +"<td>Harga</td>"
                +"<td>Total harga</td>"
            +"</tr>";
        try {
            koneksi();
            String sql = "SELECT penjualan.no_faktur, penjualan.tgljual, penjualan.kode_pelanggan, penjualan.kodebarang, produk.nama_barang, produk.harga, penjualan.jumlahjual, pelanggan.nama_pelanggan " +
             "FROM produk, penjualan, pelanggan " +
             "WHERE produk.kode_barang = penjualan.kodebarang AND pelanggan.kode_pelanggan = penjualan.kode_pelanggan AND produk.kode_barang = '" + kodeB.getText() + "';";
            Statement stat = konek.createStatement();
            ResultSet set = stat.executeQuery(sql);
            NumberFormat formatter = NumberFormat.getNumberInstance(Locale.getDefault());
            Integer totalAll = 0;
            while(set.next()) 
            {
                Integer total = Integer.parseInt(set.getString("jumlahjual")) * Integer.parseInt(set.getString("harga")) ;
                String formattedTotal = formatter.format(total);
                totalAll = totalAll + total;
                html = html + "<tr>"
                + "<td>"+set.getString("no_faktur")+"</td>"
                + "<td>"+set.getString("tgljual")+"</td>"
                + "<td>"+set.getString("jumlahjual")+"</td>"
                + "<td>"+set.getString("kode_pelanggan")+"</td>"
                + "<td>"+set.getString("nama_pelanggan")+"</td>"
                + "<td>"+set.getString("kodebarang")+"</td>"
                + "<td>"+set.getString("harga")+"</td>"
                + "<td>"+ formattedTotal +"</td>" 
                + "</tr>";
            } 
            
            String formattedTotalAll = formatter.format(totalAll);
            html = html + "<tr>"
                    + "<td colspan='7'>Total</td>"
                    + "<td>" + formattedTotalAll + "</td>"
                    + "</tr>"
                    + "</table> <br/>";
            EditorKit x = new HTMLEditorKit();
            jual.setEditorKit(x);
            jual.setText(html);
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    } 
