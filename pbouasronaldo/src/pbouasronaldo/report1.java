    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        File reportFile = new File(".");
        String dirr = "";

        try {
            // Load MySQL JDBC Driver
            Class.forName("com.mysql.jdbc.Driver");

            // Establish connection to the database
            Connection conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1/dbronaldo", "root", "");
            
            // Check if the connection is valid
            if (conn == null) {
                throw new RuntimeException("Failed to make connection!");
            }

            Statement stat = conn.createStatement();
            
            // Create a HashMap to store parameters
            HashMap parameter = new HashMap(2);
            parameter.put("kode", kodeB.getText()); 

            // SQL query to fetch data
            String sql = "SELECT * FROM penjualan";
            dirr = reportFile.getCanonicalPath() + "/src/pbouasronaldo/";

            // Load and compile the Jasper report design
            JasperDesign design = JRXmlLoader.load(dirr + "report1.jrxml");
            JasperReport jr = JasperCompileManager.compileReport(design);

            // Execute SQL query
            ResultSet rs = stat.executeQuery(sql);
            JRResultSetDataSource rsDataSource = new JRResultSetDataSource(rs);

            // Fill the report with data
            JasperPrint jp = JasperFillManager.fillReport(jr, parameter, conn);

            // Display the report
            JasperViewer.viewReport(jp, false);

            // Close resources
            rs.close();
            stat.close();
            conn.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Gagal Mencetak: " + e.getMessage(), "Kesalahan", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed

