    private void cetak() {
     String html=
                "<H4> Laporan Pengiriman</H4>"+
                "<table width='900' border='1'>"+
                "<tr>"            
                        +"<td>Id Pengiriman</td>" 
                        +"<td>Jasa Kirim</td>" 
                        +"<td>Waktu Pengiriman</td>" 
                +"</tr>";
        
        try {
            koneksi();
            String sql = "Select * from pengiriman";
            Statement stat = con.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while(rs.next()){
                html = html + "<tr>"
                       + "<td>"+rs.getString("id_pengiriman")+"</td>" 
                        +" <td>"+rs.getString("jasa_kirim")+"</td>" 
                        +" <td>"+rs.getString("waktu_pengiriman")+"</td>" 
                        +"</tr>";
            }
            EditorKit x = new HTMLEditorKit();
            Cetak.setEditorKit(x);
            Cetak.setText(html);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
